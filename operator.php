<?php

// Operator Aritmatika
$a = 10;
$b = 2;

// Penjumlahan
echo "Operator Aritmatika <br>";
$jumlah = $a + $b;
echo "Penjumlahan dari $a + $b = $jumlah";
echo "<br>";
// Pengurangan
$kurang = $a - $b;
echo "Pengurangan dari $a - $b + $kurang";
echo "<br>";
// Perkalian
$kali = $a * $b;
echo "Perkalian dari $a x $b = $kali";
echo "<br>";
// Pembagian
$bagi  = $a / $b;
echo "Perkalian dari $a / $b = $bagi";

?>